package Task::Newbie;

our $VERSION = 0.001;

1;
__END__

=head1 NAME

Task::Newbie - Make life easy for newbies

=head1 VERSION

Version 0.001

=head1 SYNOPSIS

 wget -O- http://xrl.us/cpanm | perl - Task::Newbie

=head1 WHAT THIS MODULE DOES

At the minute, not a lot, but here's what it will do:

=over 4

=item configure more sane defaults for CPAN.pm and CPANPLUS.pm

=item install the basic modules that a newbie is likely to want/need

=back

